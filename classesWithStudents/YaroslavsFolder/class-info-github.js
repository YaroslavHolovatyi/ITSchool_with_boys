//? Основні команди
//* `git init` — дозволяє ініціалізувати репозиторій в поточній папці

//* `git status` — показує поточний статус
//* `git add index.html` — відстежує зміни у index.html
//* `git add .` —  відстежує зміни в усіх файлах

//* `git commit -m 'commit message'` — створює коміт з повідомленням

//* `git checkout` — перемикається на коміт або гілку

//* `git config --global user.name` - Показує ім'я користувача
//* `git config --global user.name 'new user'` - Змінює ім'я користувача
//* `git config --global user.email` - Показує email користувача
//* `git config --global user.email 'test@mail.com'` - Змінює email користувача

//* `git push` - завантажує поточні локальні коміти в віддалений репозиторій
//* `git pull` - отримує зміни з віддаленого репозиторію в локальний
//* `git clone <посилання_на_репозиторій>`   - клонує проект з віддаленого репозиторію

//* `git branch` — показує список гілок
//* `git branch <branch-name>` — створює нову гілку branch-name
//* `git branch -D <branch-name>` — видаляє гілку branch-name

//* `git checkout <branch-name>` — перемикається на гілку branch-name
//* `git checkout -b <branch-name>` — створює і відразу перемикається на гілку branch-name

//* `git merge <branch-name>` — об'єднує поточну гілку з гілкою branch-name
